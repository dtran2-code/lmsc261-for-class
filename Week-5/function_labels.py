# int() function
# Example of calling the int() function

numberer = int("45")
print(numberer)

# Name: integer, int()
# Argument: any integer or "string" (still has to be an integer) in integer or string form
# Side Effects: nothing
# Return Value: Any integer the user typed in (as an integer or string) as an integer

####################################################

# str() function
# Example of calling the str() function

stringerer = str(45)
print(stringerer)

# Name: string, str()
# Argument: anything, to be converted into a string
# Side Effects: nothing
# Return Value: Anything the user typed in as a string type

####################################################

# bool() function
# Example of calling the bool() function

booleaner = bool("Hello, World")
print(booleaner)

# Name: boolean, bool()
# Argument: anything, to be returned as True or False
# Side Effects: nothing
# Return Value: True or False, depending on the argument

####################################################

# float() function
# Example of calling the float() function

floaterer = float(2.234)
print(floaterer)

# Name: float, float()
# Argument: Any float, integer, or "string" (still an integer or a float), to be returned as True or False
# Side Effects: nothing
# Return Value: Any float, integer, or "string" (still has to  be an integer or float) as a float

####################################################

# len() function
# Example of calling the len() function

lengthener = len("Hello, World")
print(lengthener)

# Name: length, len()
# Argument: anything, to be returned it's length
# Side Effects: nothing
# Return Value: the "length" of the argument, including spaces and punctuation

####################################################

# pow() function
# Example of calling the pow() function

powerer = pow(3,2)
print(powerer)

# Name: power, pow()
# 1st Argument: any integer or float as the base | 2nd Argument: any integer or float as the exponent
# Side Effects: nothing
# Return Value: Exponentiates the base, calculates the argument

####################################################

# abs() function
# Example of calling the abs() function

absoluter = abs(-123.23)
print(absoluter)

# Name: absolute value, abs()
# Argument: any int or float, negative or positive, to be returned the absolute value of that number
# Side Effects: nothing
# Return Value: the absolute value of the number in the argument

